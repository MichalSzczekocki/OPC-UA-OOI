{
  /// <summary>
  /// Defines values for the priorities used by a logger.
  /// </summary>
  public enum Priority
  {
    /// <summary>
    /// No priority specified.
    /// </summary>
    None = 0,

    /// <summary>
    /// High priority entry.
    /// </summary>
    High = 1,

    /// <summary>
    /// Medium priority entry.
    /// </summary>
    Medium = 2,

    /// <summary>
    /// Low priority entry.
    /// </summary>
    Low = 3
  }
}